let data="33ab"

// it will print string
console.log(typeof data);
// it will print string as well because both methods are same
console.log(typeof(data));

let value=Number(data)
// it will print number
console.log(typeof value);
// it will print NaN which is not a number 
console.log(value)

// value coversion
// "33"=>string
// 33 =>number
// true=>1
// false=>0
// null=0
// undefined=NaN
